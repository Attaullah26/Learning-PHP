<?php
$test = "hello BABAR writes BABAR";

// string postion
echo "<b>strpos :</b> " . strpos($test, "babar") . "<br>";
// string reverse positon
echo "<b>strrpos :</b> " . strrpos($test, "babar") . "<br>";


// strin insensitive position
echo "<b>stripos :</b> " . stripos($test, "babar") . "<br>";
// string reverse insensitive position
echo "<b>strripos :</b> " . strripos($test, "babar") . "<br>";




?>